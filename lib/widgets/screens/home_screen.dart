            Text('–ó–∞–¥–∞–Ω–∏—è: ${_gameManager.availableQuests.length} –¥–æ—Å—Ç—É–ø–Ω–æ'),
            Text('–í—ã–ø–æ–ª–Ω–µ–Ω–æ: ${_gameManager.completedQuests.length}'),
          ],
        ),
        duration: const Duration(seconds: 3),
      ),
    );
  }

  @override
  Widget build(BuildContext context) {
    if (_isLoading) {
      return Scaffold(
        backgroundColor: Theme.of(context).colorScheme.background,
        body: Center(
          child: Column(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              const CircularProgressIndicator(),
              const SizedBox(height: 20),
              Text(
                _loadingStatus,
                style: Theme.of(context).textTheme.titleMedium?.copyWith(
                  color: Theme.of(context).colorScheme.onBackground,
                ),
              ),
              const SizedBox(height: 10),
              Text(
                '${_gameManager.allQuests.length} –∑–∞–¥–∞–Ω–∏–π',
                style: Theme.of(context).textTheme.bodySmall?.copyWith(
                  color: Theme.of(context).colorScheme.onBackground.withOpacity(0.7),
                ),
              ),
            ],
          ),
        ),
      );
    }

    // –í–´–ß–ò–°–õ–Ø–ï–ú –î–ê–ù–ù–´–ï
    final progressPercentage = _calculateProgressPercentage();
    final achievedCount = _calculateAchievedCount();
    final allQuestsCompleted = _checkAllQuestsCompleted();

    return Scaffold(
      appBar: AppBar(
        title: const Text('üß™ –•–∏–º–∏—á–µ—Å–∫–∏–π —Å–∏–º—É–ª—è—Ç–æ—Ä'),
