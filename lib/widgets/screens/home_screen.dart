  @override
  void initState() {
    super.initState();
    _initializeGame();
  }

  Future<void> _initializeGame() async {
    try {
      print('üéÆ Starting game initialization...');
      setState(() {
        _loadingStatus = '–ó–∞–≥—Ä—É–∂–∞–µ–º –≤–µ—â–µ—Å—Ç–≤–∞...';
      });
      
      await _gameManager.initializeGameData();
      
      setState(() {
        _loadingStatus = '–ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–æ–≥—Ä–µ—Å—Å...';
      });
      
      _gameManager.printDebugInfo();
      
      print('‚úÖ Game initialization complete');
      
    } catch (e) {
      print('‚ùå Error initializing game: $e');
      _showErrorDialog(e.toString());
    } finally {
      setState(() {
        _isLoading = false;
      });
    }
  }

  void _showErrorDialog(String error) {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏'),
        content: Column(
          mainAxisSize: MainAxisSize.min,
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            const Text('–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å –¥–∞–Ω–Ω—ã–µ –∏–≥—Ä—ã.'),
            const SizedBox(height: 10),
            Text('–û—à–∏–±–∫–∞: $error', style: const TextStyle(fontSize: 12, color: Colors.grey)),
          ],
        ),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(context),
            child: const Text('–ü–æ–Ω—è—Ç–Ω–æ'),
          ),
          TextButton(
            onPressed: () {
              Navigator.pop(context);
              _retryInitialization();
            },
            child: const Text('–ü–æ–≤—Ç–æ—Ä–∏—Ç—å'),
          ),
        ],
      ),
    );
  }

  void _retryInitialization() {
    setState(() {
      _isLoading = true;
      _loadingStatus = '–ü–æ–≤—Ç–æ—Ä–Ω–∞—è –∑–∞–≥—Ä—É–∑–∫–∞...';
    });
    _initializeGame();
  }

  void _navigateToQuestSelection() {
    if (!_gameManager.isInitialized) {
      _showNotInitializedDialog();
      return;
    }
    
    Navigator.push(
      context,
      MaterialPageRoute(
        builder: (context) => QuestSelectionScreen(
          gameManager: _gameManager,
          onQuestCompleted: _onQuestCompleted,
        ),
      ),
    );
  }

  void _onQuestCompleted() {
    setState(() {});
    print('üîÑ HomeScreen: Quest completed, refreshing state...');
  }

  void _navigateToProgress() {
    if (!_gameManager.isInitialized) {
      _showNotInitializedDialog();
      return;
    }
    
    Navigator.push(
      context,
      MaterialPageRoute(
        builder: (context) => ProgressScreen(gameManager: _gameManager),
      ),
    );
  }

  void _navigateToAchievements() {
    if (!_gameManager.isInitialized) {
      _showNotInitializedDialog();
      return;
    }
    
    Navigator.push(
      context,
      MaterialPageRoute(
        builder: (context) => AchievementsScreen(gameManager: _gameManager),
      ),
    );
  }

  void _navigateToSettings() {
    Navigator.push(
      context,
      MaterialPageRoute(builder: (context) => const SettingsScreen()),
    );
  }

  void _showNotInitializedDialog() {
    showDialog(
      context: context,
      builder: (context) => AlertDialog(
        title: const Text('–î–∞–Ω–Ω—ã–µ –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω—ã'),
