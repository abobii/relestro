                    decoration: BoxDecoration(
                      color: color.withOpacity(0.1),
                      shape: BoxShape.circle,
                    ),
                    child: Icon(icon, size: 18, color: color),
                  ),
                  
                  const SizedBox(height: 6),
                  
                  // –ó–∞–≥–æ–ª–æ–≤–æ–∫
